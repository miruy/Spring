package survey;

public class Respondent {	//Respondent : 설문조사 응답자의 정보를 담기 위한 클래스 
	private int age;
	private String location;	//위치
	
	public int getAge() {
		return age;
	}
	public void setAge(int age) {
		this.age = age;
	}
	public String getLocation() {
		return location;
	}
	public void setLocation(String location) {
		this.location = location;
	}
	
	
}
